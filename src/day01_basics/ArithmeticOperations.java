package day01_basics;

public class ArithmeticOperations {
	public static void main(String[] args) {
		int x = 20;
		int y = 10;
		
		System.out.println(x+" + "+y+" = " + (x+y));
		System.out.println(x+" - "+y+" = " + (x-y));
		System.out.println(x+" * "+y+" = " + (x*y));
		System.out.println(x+" / "+y+" = "+ (x/y));
		System.out.println(x+" % "+y+" = "+ (x%y));

	}
}
